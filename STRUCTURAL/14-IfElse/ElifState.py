# Else IF statement (elif)

nama = input("Nama kamu siapa? ")

if nama=="ucup": #kondisi 1
        print("hai gantengg") #aksi true satu
elif nama=="otong": #kondisi 2
        print("woe anjengggg") #aksi true 2
elif nama=="mario": #kondisi 3
        print("hoolaa britaaa") #aksi true 3
else:
        print("tau ah ga kenal") #aksi false

print("\nini adalah akhir dari program")
